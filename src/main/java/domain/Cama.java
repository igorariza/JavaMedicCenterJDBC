package domain;

public class Cama {

    private String numero_de_cama;
    private String descripcion_cama;
    private String area_cama;
    private String estado_cama;

    public String getNumero_de_cama() {
        return numero_de_cama;
    }

    public void setNumero_de_cama(String numero_de_cama) {
        this.numero_de_cama = numero_de_cama;
    }

    public String getDescripcion_cama() {
        return descripcion_cama;
    }

    public void setDescripcion_cama(String descripcion_cama) {
        this.descripcion_cama = descripcion_cama;
    }

    public String getArea_cama() {
        return area_cama;
    }

    public void setArea_cama(String area_cama) {
        this.area_cama = area_cama;
    }

    public String getEstado_cama() {
        return estado_cama;
    }

    public void setEstado_cama(String estado_cama) {
        this.estado_cama = estado_cama;
    }

}
